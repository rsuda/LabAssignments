package datautils;

/**
 * This enumerated type specifies different ordering configurations for an array. 
 * 
 * @author bdorn
 *
 */
public enum ArrayOrder
{
	/**
	 * Signifies ascending initial order of an array.
	 */
	INORDER,
	
	/**
	 * Signifies descending initial order of an array.
	 */
	REVERSE,
	
	/**
	 * Signifies random initial ordering of an array.
	 */
	RANDOM
}
